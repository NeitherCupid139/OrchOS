function applyReplacements(value: string, replacements: readonly Replacement[]) {
  let next = value;

  for (const [from, to] of replacements) {
    next = next.split(from).join(to);
  }

  return next;
}

export function applyScriptPreferences(
  value: string,
  locale: string,
  settings: Pick<ControlSettings, "useMixedScript" | "preferKanji"> | null | undefined,
) {
  if (!value) {
    return value;
  }

  if (locale === "ko" && settings?.useMixedScript) {
    return applyReplacements(value, KO_REPLACEMENTS);
  }

  if (locale === "ja" && settings?.preferKanji) {
    return applyReplacements(value, JA_REPLACEMENTS);
  }

  return value;
}
